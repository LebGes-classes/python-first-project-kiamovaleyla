import os


class Menu:
    """Класс для управления меню игры."""

    @staticmethod
    def clear_screen():
        """Очистка экрана консоли."""

        os.system('cls' if os.name == 'nt' else 'clear')

    @staticmethod
    def show_main_menu():
        """Отображение главного меню."""

        Menu.clear_screen()

        print("╔══════════════════════════════════╗")
        print("║           ЛАБИРИНТ               ║")
        print("╠══════════════════════════════════╣")
        print("║ 1. Новая игра                    ║")
        print("║ 2. Управление                    ║")
        print("║ 3. Об игре                       ║")
        print("║ 4. Выход                         ║")
        print("╚══════════════════════════════════╝")

        while True:
            choice = input("Выберите вариант (1-4): ")
            if choice in ['1', '2', '3', '4']:

                return int(choice)

            print("Неверный ввод. Попробуйте снова.")

    @staticmethod
    def show_controls():
        """Отображение управления."""

        Menu.clear_screen()

        print("╔══════════════════════════════════╗")
        print("║          УПРАВЛЕНИЕ              ║")
        print("╠══════════════════════════════════╣")
        print("║ W или ↑ - Движение вверх         ║")
        print("║ S или ↓ - Движение вниз          ║")
        print("║ A или ← - Движение влево         ║")
        print("║ D или → - Движение вправо        ║")
        print("║ Q - Выход в меню                 ║")
        print("╚══════════════════════════════════╝")
        input("\nНажмите Enter для возврата в меню...")

    @staticmethod
    def show_about():
        """Отображение информации об игре."""

        Menu.clear_screen()

        print("╔══════════════════════════════════╗")
        print("║           ОБ ИГРЕ                ║")
        print("╠══════════════════════════════════╣")
        print("║ Лабиринт - консольная игра       ║")
        print("║ Цель: найти выход из лабиринта   ║")
        print("║                                  ║")
        print("║ Обозначения:                     ║")
        print("║   P - игрок                      ║")
        print("║   █ - стена                      ║")
        print("║   E - выход                      ║")
        print("║   · - проход                     ║")
        print("╚══════════════════════════════════╝")
        input("\nНажмите Enter для возврата в меню...")

    @staticmethod
    def show_game_screen(maze_display, moves_count):
        """Отображение игрового экрана."""

        Menu.clear_screen()

        print("╔══════════════════════════════════╗")
        print("║         ИГРА: ЛАБИРИНТ           ║")
        print("╠══════════════════════════════════╣")

        for row in maze_display:
            # Заменяем пробелы на точки для лучшей видимости проходов
            display_row = row.replace(' ', '·')

            print(f"║ {display_row} ║")

        print("╠══════════════════════════════════╣")
        print(f"║ Ходов сделано: {moves_count:<19} ║")
        print("║ Управление: WASD/Стрелки, Q - меню║")
        print("╚══════════════════════════════════╝")
